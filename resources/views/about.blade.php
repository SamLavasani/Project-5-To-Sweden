<h1 id="about" class="text-center mt-5">Ett svenskt vinter-OS ska bli den inspirationskälla vi vill att OS ska vara:</h1>
        <div class="container">         
        <hr class="hr" size=1   color="#ffcc00" style="border:2 solid #ffcc00">
        <p class="px-5 darkblue-bg p-4 m-2 rounded"> 
        <strong>Samlande.</strong> 
        <br> 
        Själva grunden i Pierre de Coubertins olympiska tanke är dess samlande kraft. 
        Spelen samlar de bästa idrottarna från hela världen; de tävlar tillsammans, bor tillsammans och äter tillsammans. 
        Det är symbolen för att fredlig samexistens är möjlig över alla gränser även i turbulenta tider. "Unity in Diversity". 
        <br><br>
       <strong>Hållbara. </strong> 
        <br> 
        De olympiska och paralympiska spelen har förändrats. Den olympiska rörelsens nya strategi Agenda 2020, 
        med dess högt ställda ambitioner om social, ekonomisk, miljömässig och demokratisk hållbarhet, 
        passar väl in på Stockholms och Sveriges profil. 
        Vinterspelen är en chans att visa upp ett område där vi ur flera aspekter är världsledande, 
        samtidigt som projektet både kan kräva och skapa inspiration för ännu mer ansträngning för att befästa tätpositionen. 
        Det är också en chans för oss att sätta en tydlig prägel på spelen och visa hur hållbarhet kan bli en integrerad konkret del av den olympiska och paralympiska tanken, 
        ett internationellt föredöme. Vi kan skapa hållbara vinterspel som kan inspirera och samla den olympiska och paralympiska rörelsen, precis som Stockholm gjorde 1912.
        <br><br>
        <strong>Vitaliserande. </strong> 
        <br>
        OS och Paralympics ger en möjlighet till vitalisering av svensk topp-, tävlings- och ungdomsidrott. 
        Vinterspelen blir en hävstång för att utveckla idrotten. För det krävs en rejäl satsning från alla inblandade parter: 
        idrottsrörelsen, staden, staten och näringslivet. 
        Med ett samlat engagemang kan OS och Paralympics ge skjuts till att fler ska träna mer, 
        satsa hårdare och nå bättre resultat. 
        Stödet till detta kan och behöver förbättras så att fler tjejer och killar ska ges möjligheter att satsa fullt ut och för att Sverige ska kunna behålla och utveckla den internationella konkurrensnivån. 
        Efter spelen kommer bättre anläggningar att finnas för både tävlings-, ungdoms- och breddidrotten. 
        Stockholm kommer att i direkt anslutning till innerstaden ha anläggningar för skridskoåkning, 
        längdskidåkning och betydligt bättre förutsättningar för utförsåkning.
        <br><br>
        <strong>Inspirerande.</strong> 
        <br>
        Vinterspelen kan inspirera många människor till en aktiv livsstil även i storstad, även utomhus och även på vintern. 
        Uppmärksamhet på värdet av god hälsa skapas och idrott placeras mitt i det offentliga samtalet under nästan tio års tid. 
        Vinterspelen kommer att motivera idrottare, inte minst ungdomar, till att vilja göra mer och prova nytt. 
        Fleråriga satsningar på nya unga ledare, ”OS i skolan”, 
        prova på aktiviteter för barn och ungdomar samt nya idrottsformer i icke idrottstraditionella miljöer och stadsdelar ges 
        ett sammanhang. Vinterspel på hemmaplan ger inte bara kraft åt Paralympics och paraidrottare med siktet inställt på att 
        delta själva, utan fäster uppmärksamhet på tillgänglighet både för idrott och ur ett större samhälleligt perspektiv.
            
            
